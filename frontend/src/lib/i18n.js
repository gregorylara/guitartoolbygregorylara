// ── State ─────────────────────────────────────────────────────────────────────
let _lang = localStorage.getItem('guitartool_lang') || 'es';

export function getLang() { return _lang; }

export function setLang(lang) {
  if (lang === _lang) return;
  _lang = lang;
  localStorage.setItem('guitartool_lang', lang);
  window.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

/**
 * t(key) — returns translated string for current language,
 * falling back to English if key is missing.
 */
export function t(key) {
  return TRANSLATIONS[_lang]?.[key]
      ?? TRANSLATIONS['en']?.[key]
      ?? key;
}
